    public void initEdit() {
        fontList.setCellRenderer(new ListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                JLabel j = new JLabel(String.valueOf(value));
                j.setText((String) value);
                j.setOpaque(true);
                j.setBounds(0, 0, 0, 30);
                if (cellHasFocus) {
                    j.setBackground(new Color(238, 238, 238));
                } else if (isSelected) {
                    j.setBackground(new Color(240, 240, 240));
                }
                return j;
            }
        });
        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
        Font[] fonts = ge.getAllFonts();
        DefaultListModel d = new DefaultListModel();
        for (int i = 0; i < fonts.length; i++) {
            d.addElement(fonts[i].getName());
        }
        fontList.setModel(d);
        fontList.repaint();
        fontList1.setModel(d);
        fontList1.repaint();
        floater.repaint();
        floater.hide();

        UIManager.LookAndFeelInfo[] ll = UIManager.getInstalledLookAndFeels();
        DefaultListModel dlm = new DefaultListModel();
        for (int i = 1; i < ll.length; i++) {
            dlm.addElement(ll[i].getClassName());
        }
        themes.setModel(dlm);
        logEditor.setBackground(Color.WHITE);
    }

