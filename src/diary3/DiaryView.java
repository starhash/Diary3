    public void setEditMenu(String code) {
        System.out.println("enter");
        if (code.equals("Edit")) {
            editMenu.removeAll();
            editMenu.setLayout(new GridLayout());
            editMenu.add(defaultEditMenu);
            defaultEditMenu.show();
            System.out.println("done");
        } else if (code.equals("User")) {
            menu.setSelectedIndex(0);
            floater.show();
            System.out.println("done user");
            return;
        } else if (code.equals("Desk")) {
            editMenu.removeAll();
            editMenu.setLayout(new GridLayout());
            editMenu.add(deskEditMenu);
            deskEditMenu.show();
            System.out.println("done");
        } else if (code.equals("Log")) {
            editMenu.removeAll();
            editMenu.setLayout(new GridLayout());
            editMenu.add(logEditMenu);
            deskEditMenu.show();
            System.out.println("done");
        } else if (code.equals("HTML")) {
            logEditTab.setSelectedIndex(1);
            floater.show();
            System.out.println("done user");
            return;
        } else if (code.equals("None")) {
            editMenu.removeAll();
        }
        floater.show();
        menu.setSelectedIndex(2);
    }

