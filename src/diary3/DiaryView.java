    private void currentLogListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_currentLogListValueChanged
        // TODO add your handling code here:
        logEditor.setText(Diary3.CURRENT_USER.readSelectedLog());
        documentEditor1.setText(Diary3.CURRENT_USER.readSelectedLog().replaceAll("\n", "<br>"));
        String n = (String) (currentLogList.getSelectedValue());
        if (n.contains(".logx")) {
            n = n.substring(0, n.lastIndexOf(".logx"));
        }
        String time = n.substring(0, n.indexOf("."));
        StringTokenizer st = new StringTokenizer(time, "-");
        String h = (st.nextToken());
        String m = (st.nextToken());
        String s = (st.nextToken());
        String title = n.substring(n.indexOf(".") + 1);
        title = new JuliusCrypter().perfectSquareCrypt(title);
        String yr = logCalendar.years.getSelectedItem().toString();
        String mth = (logCalendar.months.getSelectedIndex() + 1) + "";
        int x = logCalendar.dates.getSelectedRow();
        int y = logCalendar.dates.getSelectedColumn();
        final String value = (String) logCalendar.dates.getModel().getValueAt(x, y);
        String date = value.substring(0, value.indexOf(" ")).trim();
        int hi = Integer.parseInt(h);
        int mi = Integer.parseInt(m);
        int si = Integer.parseInt(s);
        int yi = Integer.parseInt(yr);
        int moi = Integer.parseInt(mth);
        int di = Integer.parseInt(date);
        Date d = new Date(yi, moi, di, hi, mi-1, si);
        titleView.setText(title);
        logEditorWindow.setTitle(d.toString()+" - "+title);
        desk.repaint();
    }//GEN-LAST:event_currentLogListValueChanged

