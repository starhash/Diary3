    public void sortOutEntries() {
        fma.readEntries();
        FinanceEntry[] entries = new FinanceEntry[fma.financeEntries.size()];
        System.out.println("Total Entries : " + entries.length);
        for (int i = 0; i < entries.length; i++) {
            entries[i] = fma.financeEntries.get(i);
        }
            int da = date.getSelectedIndex();
            String m = (String) month.getSelectedItem();
            int yr = year.getSelectedIndex();
            Vector monthfilter = new Vector();
            for (int i = 0; i < entries.length; i++) {
                FinanceEntry f = entries[i];
                if (m.contains("---")) {
                    monthfilter.addElement(f);
                    System.out.println("MWild : " + f.date);
                } else {
                    if (f.date.contains(m)) {
                        monthfilter.addElement(f);
                        System.out.println("MFilter : " + f.date);
                    }
                }
            }
            System.out.println(monthfilter);
            int n = monthfilter.size();
            System.out.println("MonthFilter Count : " + n);
            Vector datefilter = new Vector();
            for (int i = 0; i < n; i++) {
                FinanceEntry f = (FinanceEntry) monthfilter.get(i);
                if (da == 31) {
                    datefilter.addElement(f);
                    System.out.println(i + " DWild : " + f.date);
                } else {
                    System.out.println("else date");
                    System.out.println(new Date(f.date).getDate() + " : " + (da + 1));
                    if (new Date(f.date).getDate() == da + 1) {
                        datefilter.addElement(f);
                        System.out.println(i + " DFilter : " + f.date);
                    }
                }
            }
            System.out.println(datefilter);
            Vector yearfilter = new Vector();
            for (int i = 0; i < datefilter.size(); i++) {
                FinanceEntry f = (FinanceEntry) datefilter.elementAt(i);
                if (yr == 0) {
                    yearfilter.addElement(f);
                    System.out.println("YWild : " + f.date);
                } else {
                    if (f.date.contains((String) year.getSelectedItem())) {
                        yearfilter.addElement(f);
                        System.out.println("YFilter : " + f.date);
                    }
                }
            }
            System.out.println("YearFilter count : " + yearfilter.size());
            System.out.println(yearfilter);
            DefaultListModel ff = new DefaultListModel();
            for (int i = 0; i < yearfilter.size(); i++) {
                FinanceEntry fe = (FinanceEntry) yearfilter.elementAt(i);
                ff.addElement(fe);
            }
            jList1.setModel(ff);
            jList1.repaint();
    }

