    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        System.out.println(jList1.getSelectedValue().toString());
        StringTokenizer stt = new StringTokenizer(jList1.getSelectedValue().toString(), "|");
        String tag = stt.nextToken();
        String ds = stt.nextToken();
        double amt = Double.parseDouble(stt.nextToken());
        char c = stt.nextToken().charAt(0);
        String desc = stt.nextToken();
        FinanceEntry f = new FinanceEntry(ds, tag, amt, c, desc);
        tagEntryView.setText(f.tag);
        amountEntryView.setText(f.amount+"");
        dORcEntryView.setSelectedIndex((f.dORc=='d')?0:1);
        descriptionEntryView.setText(f.description);
        String date = f.date;
        Date dd = new Date(date);
        dateEntryView.setText(dd.getDate()+"");
        monthEntryView.setText((dd.getMonth()+1)+"");
        yearEntryView.setText((dd.getYear()+1900)+"");
        hoursTicker.setValue(dd.getHours());
        minutesTicker.setValue(dd.getMinutes());
        secondsTicker.setValue(dd.getSeconds());
        jLabel9.setText("Locked");
        submitEntryView.setEnabled(false);
    }//GEN-LAST:event_jList1ValueChanged

