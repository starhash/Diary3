    private void submitEntryViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEntryViewActionPerformed
        // TODO add your handling code here:
        int year = Integer.parseInt(yearEntryView.getText());
        int month = Integer.parseInt(monthEntryView.getText());
        int date = Integer.parseInt(dateEntryView.getText());
        int hrs = (Integer) hoursTicker.getValue();
        int min = (Integer) minutesTicker.getValue();
        int sec = (Integer) secondsTicker.getValue();
        DefaultComboBoxModel d = (DefaultComboBoxModel) this.month.getModel();
        String hs = (hrs < 10) ? "0" + hrs : hrs + "";
        String ms = (min < 10) ? "0" + min : min + "";
        String ss = (sec < 10) ? "0" + sec : sec + "";

        String s = date + " " + d.getElementAt(month - 1) + " " + year + " " + hs + ":" + ms + ":" + ss;
        fma.addEntry(s, tagEntryView.getText(), Double.parseDouble(amountEntryView.getText()), (dORcEntryView.getSelectedIndex() == 0) ? 'd' : 'c', descriptionEntryView.getText());
        System.out.println("Added!");
        jLabel9.setText("Locked");
        submitEntryView.setEnabled(false);
        sortOutEntries();
    }//GEN-LAST:event_submitEntryViewActionPerformed

